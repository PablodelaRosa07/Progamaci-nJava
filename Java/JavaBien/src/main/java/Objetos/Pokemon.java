package Objetos;

public class Pokemon {
    //Constructores
    String nombre;
    String tipo;
    String vida;
    String evoluciones;

    public Pokemon(String nombre, String tipo, String vida, String evoluciones) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.vida = vida;
        this.evoluciones = evoluciones;
    }
}
